function animaPos() {
  const posArr = [];
  for (let i = 0; i < 5; ++i) {
    for (let j = i; j < 10 - i; ++j) {
      posArr.push([i, j]);
    }
    for (let j = i + 1; j < 20 - i; ++j) {
      posArr.push([j, 9 - i]);
    }
    for (let j = 8 - i; j >= i; --j) {
      posArr.push([19 - i, j]);
    }
    for (let j = 18 - i; j > i; --j) {
      posArr.push([j, i]);
    }
  }
  return posArr;
}

